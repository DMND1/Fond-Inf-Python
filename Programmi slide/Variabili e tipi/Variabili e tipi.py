pezziVenduti = 0                        # definizione e assegnazione di un valore
                                        # il nome è pezziVenduti, il valore è 0
                                        
pezziVenduti = 6                        # assegnazione di un nuovo valore

                                        # il segno = è l'operatore di assegnazione
                                        # da non confondere con il segno = usato in algebra
                                        
pezziVenduti = pezziVenduti + 2         # l'espressione a destra viene valutata, stiamo parlando del valore della variabile
                                        # l'espressione a sinistra non viene valutata, stiamo definendo una variabile
                                        # il valore risultante (8) viene asseganto alla variabile
                                        
print(pezziVenduti)                     # stampa 8
                                        # anche dentro print() la varaibile viene valutata
